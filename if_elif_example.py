# Example for usage of elif (else if) in python

name = 'Bob'
age = 3000

if name == 'Som':
    print('Hi Som!')
elif age < 12:
    print('You are not Som, kiddo.')
elif age > 2000:
    print('Unlike you, Som is not an undead, immortal vampire.')
elif age > 100:
    print('You are not Som, grannie.')
