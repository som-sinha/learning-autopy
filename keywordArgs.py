# Keyword arguments are optional arguments in a function
# None value is data type which means nothing (literally)

print('Hello')
print('World')
print('')       # print automatically adds a newline everytime

print('Hello', end = '')        #keyword arguments allows to chose a different end
print('World')

print('Spam Egg Spam Spam', end = ' ')
print('Bacon', 'Spam',  sep = ' and ')
